package com.mycompany.app;
public class JudicialBranch{
    public String highest_courts;
    public String judge_selection_and_term_of_office;
    public String subordinate_courts;

    public String getHighest_courts() {
        return highest_courts;
    }

    public void setHighest_courts(String highest_courts) {
        this.highest_courts = highest_courts;
    }

    public String getJudge_selection_and_term_of_office() {
        return judge_selection_and_term_of_office;
    }

    public void setJudge_selection_and_term_of_office(String judge_selection_and_term_of_office) {
        this.judge_selection_and_term_of_office = judge_selection_and_term_of_office;
    }

    public String getSubordinate_courts() {
        return subordinate_courts;
    }

    public void setSubordinate_courts(String subordinate_courts) {
        this.subordinate_courts = subordinate_courts;
    }
}
