package com.mycompany.app;
public class TelephoneSystem{
    public String general_assessment;
    public String domestic;
    public String international;

    public String getGeneral_assessment() {
        return general_assessment;
    }

    public void setGeneral_assessment(String general_assessment) {
        this.general_assessment = general_assessment;
    }

    public String getDomestic() {
        return domestic;
    }

    public void setDomestic(String domestic) {
        this.domestic = domestic;
    }

    public String getInternational() {
        return international;
    }

    public void setInternational(String international) {
        this.international = international;
    }
}
