package com.mycompany.app;
public class MaritimeClaims{
    public String territorial_sea;
    public String exclusive_economic_zone;
    public String continental_shelf;

    public String getTerritorial_sea() {
        return territorial_sea;
    }

    public void setTerritorial_sea(String territorial_sea) {
        this.territorial_sea = territorial_sea;
    }

    public String getExclusive_economic_zone() {
        return exclusive_economic_zone;
    }

    public void setExclusive_economic_zone(String exclusive_economic_zone) {
        this.exclusive_economic_zone = exclusive_economic_zone;
    }

    public String getContinental_shelf() {
        return continental_shelf;
    }

    public void setContinental_shelf(String continental_shelf) {
        this.continental_shelf = continental_shelf;
    }
}
