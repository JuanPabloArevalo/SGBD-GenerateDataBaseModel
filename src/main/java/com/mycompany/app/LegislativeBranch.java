package com.mycompany.app;
public class LegislativeBranch{
    public String description;
    public String elections;
    public String election_results;

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getElections() {
        return elections;
    }

    public void setElections(String elections) {
        this.elections = elections;
    }

    public String getElection_results() {
        return election_results;
    }

    public void setElection_results(String election_results) {
        this.election_results = election_results;
    }
}
