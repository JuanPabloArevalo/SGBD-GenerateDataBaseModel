package com.mycompany.app;
public class DiplomaticRepresentationInTheUs{
    public String chief_of_mission;
    public String chancery;
    public String telephone;
    public String fax;
    public String consulate_s_general;
    public String consulate_s;

    public String getChief_of_mission() {
        return chief_of_mission;
    }

    public void setChief_of_mission(String chief_of_mission) {
        this.chief_of_mission = chief_of_mission;
    }

    public String getChancery() {
        return chancery;
    }

    public void setChancery(String chancery) {
        this.chancery = chancery;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getFax() {
        return fax;
    }

    public void setFax(String fax) {
        this.fax = fax;
    }

    public String getConsulate_s_general() {
        return consulate_s_general;
    }

    public void setConsulate_s_general(String consulate_s_general) {
        this.consulate_s_general = consulate_s_general;
    }

    public String getConsulate_s() {
        return consulate_s;
    }

    public void setConsulate_s(String consulate_s) {
        this.consulate_s = consulate_s;
    }
}
