package com.mycompany.app;
public class Elevation{
    public String mean_elevation;
    public String lowest_point;
    public String highest_point;

    public String getMean_elevation() {
        return mean_elevation;
    }

    public void setMean_elevation(String mean_elevation) {
        this.mean_elevation = mean_elevation;
    }

    public String getLowest_point() {
        return lowest_point;
    }

    public void setLowest_point(String lowest_point) {
        this.lowest_point = lowest_point;
    }

    public String getHighest_point() {
        return highest_point;
    }

    public void setHighest_point(String highest_point) {
        this.highest_point = highest_point;
    }
}
