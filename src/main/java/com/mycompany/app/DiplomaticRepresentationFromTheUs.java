package com.mycompany.app;
public class DiplomaticRepresentationFromTheUs{
    public String chief_of_mission;
    public String telephone;
    public String embassy;
    public String mailing_address;
    public String fax;

    public String getChief_of_mission() {
        return chief_of_mission;
    }

    public void setChief_of_mission(String chief_of_mission) {
        this.chief_of_mission = chief_of_mission;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getEmbassy() {
        return embassy;
    }

    public void setEmbassy(String embassy) {
        this.embassy = embassy;
    }

    public String getMailing_address() {
        return mailing_address;
    }

    public void setMailing_address(String mailing_address) {
        this.mailing_address = mailing_address;
    }

    public String getFax() {
        return fax;
    }

    public void setFax(String fax) {
        this.fax = fax;
    }
}
